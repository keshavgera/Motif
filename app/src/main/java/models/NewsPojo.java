public class NewsPojo {

    public String newsId;
    public String newsHead;
    public String newsDesc;
    public String newsTo;
    public String newsDate;
    public String newsFrom;
    public String newsImgage;

    public String getNewsImgage() {
        return newsImgage;
    }

    public void setNewsImgage(String newsImgage) {
        this.newsImgage = newsImgage;
    }

    public String getNewsId() {
        return newsId;
    }

    public void setNewsId(String newsId) {
        this.newsId = newsId;
    }

    public String getNewsHead() {
        return newsHead;
    }

    public void setNewsHead(String newsHead) {
        this.newsHead = newsHead;
    }

    public String getNewsDesc() {
        return newsDesc;
    }

    public void setNewsDesc(String newsDesc) {
        this.newsDesc = newsDesc;
    }

    public String getNewsTo() {
        return newsTo;
    }

    public void setNewsTo(String newsTo) {
        this.newsTo = newsTo;
    }

    public String getNewsDate() {
        return newsDate;
    }

    public void setNewsDate(String newsDate) {
        this.newsDate = newsDate;
    }

    public String getNewsFrom() {
        return newsFrom;
    }

    public void setNewsFrom(String newsFrom) {
        this.newsFrom = newsFrom;
    }
}
